<h4>
Les opérateurs de sites Web utilisant Shariff protègent la vie privée de leurs visiteurs de la curiosité excessive des réseaux sociaux tels que Facebook, Google+ et Twitter. Pour les visiteurs un clic suffit pour partager une page avec des amis.
Vue agrandie
</h4>
<p>Avec Shariff, vous pouvez utiliser les médias sociaux sans inutilement
mettre en péril votre confidentialité.
Le projet Shariff de ct a remplacé une bonne partie des habituels boutons
des réseaux sociaux et protège vos habitudes de surf des regards indiscrets.
Toutefois, un seul clic sur le bouton suffit pour partager des informations avec les autres.
Vous devez juste prendre conscience cela - le webmaster a déjà pris soin de tout.</p>
<p>
Les boutons habituels des médias sociaux, transférent 
les données des utilisateur à chaque demande de page Facebook &amp; co. 
et fourni des informations précises sur votre comportement de surf
aux réseaux sociaux (suivi de l’utilisateur).
Pour ce faire, vous devez soit être connecté en étant un membre du réseau.
Toutefois, depuis seulement un bouton de partage qui établit un contact 
direct entre le réseau social et les visiteurs si ce dernier 
a activement cliqué sur le bouton partager.
</p><p>
Shariff empêche donc de laissez une piste numérique sur chaque page que vous visitez
et améliore la protection des données.
Ensuite, vous pouvez assimiler discrétion, + 1er ou Tweet - plus ne reçoit pas le réseau social.
</p><p>
L’affichage des «j'aime» vient grâce à l’utilisation des boutons Shariff.
vous devez faire confiance en tant que visiteur,
Car Shariff est incorporé par le webmaster.
</p>
<h3>D’avantage de vie privée avec Shariff</h3>
<p>
Les boutons de partage communs représentent un problème important de respect de la vie privée,
car ils ont un contact avec les serveurs du reseau social inaperçu.
Ainsi, par exemple, Facebook envoie le nombre de « likes » sur le navigateur du visiteur et consigné dans le comportement de surf individuels.
</p><p>
Shariff ici se présente sous forme d’une instance intermédiaire : 
à la place le navigateur, 
le serveur de l’opérateur du site Web demande le nombre de like en off
et ce 1 seule fois par minute pour limiter le trafic. 
Ici, le visiteur reste anonyme.
</p><p>
Shariff simplifie et remplace la précédente solution et a été présenté 
trois ans auparavant dans le magazine ct pour plus d’intimité.
</p>
