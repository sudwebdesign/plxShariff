<h3>Confidentialité, partage, médias sociaux, les boutons de médias sociaux</h3>
<p>Un site charge les boutons officiels de partage de Facebook, Google+ et Twitter, ses données personnelles leurs sont transmises. Shariff montre qu’il existe une autre façon : le successeur de la solution précédente en deux clics développé par nous, permet d’économiser un clic et est toujours compatible avec la protection des données.</p>
<p>Bon, a peu près tout le monde sur le NET peut aimer partager dans les réseaux sociaux : même sur les pages adultes sont souvent accompagné par le bouton « Like »de Facebook, le bouton de Google - « + 1" et l’oiseau de Twitter. La plupart des utilisateurs veulent partager plusieurs autres vidéos de contenu, à savoir drôles et passionnant, article, GIFs de chats migno. Pour les opérateurs de site internet, il est intéressant de faire ainsi pour fournir des fonctionnalités de partageurs : jeunes utilisateurs d’Internet peuvent généreusement distribuer vos découvertes Web sur Facebook et y rencontrer à nouveau sur les autres recommandations. Qui reçoit plus de visiteurs, plus de trafic, plus de commentaires dont les contenus sont souvent divisés,.</p>
<h3>Peu d'abonnés</h3><img src="../../plugins/plxShariff/lang/img/tn_iframes_NEU_bg_SC.jpg" />
<p>Les boutons officiels de partage de contenu sur Facebook, Google+ et Twitter sont intégrés sur de nombreux sites par iframe. La vie privée et la protection des données en Souffrent de ce systeme.</p>
<p>Habituellement , les boutons de recommandation avec les iframes sont intégrés dans des sites Web. Le code dans ce cadre vient directement de Facebook, Google et Twitter - et c'est un problème: Même lors du chargement des boutons de partage le navigateur envoie des informations personnelles telles que l'adresse IP ou les cookies stockés localement aux services sociaux. Cela se produit aussi quand un visiteur du site n'a pas de compte sur Facebook, Google et Twitter, ou que la fonction des jetons ne serait pas utilisé.</p>
<p>Les utilisateurs enregistrés prenent une douche froide : Facebook eux, à ce sujet identifie de manière unique et apprend ainsi sur quelles pages ils surfent. La société américaine évalue l’URL de la page que vous visitez et l’heure de la visite. Facebook utilisera ces renseignements, par exemple, pour des « Publicité » basée sur votre utilisation des sites Web ou applications en dehors de Facebook. Alors visitez le site de votre groupe préféré sur lequel est incorporé un bouton J’aime, voir une annonce qui fait la promotion de son nouvel album plus tard sur Facebook. Quand ses annonces « adaptés » pour lequel les visites de la page Web de l’entreprise visites sont évalués avec l’aide des boutons de « traque » Twitter, Twitter même rêgles. Alors que Facebook et Twitter permettent un opt-out, de sorte que le comportement de surf ne serve pas de base a la publicité. Cela n'empêche toutefois pas leurs Traqueur (ndt : lors d'un partage).</p>
<h3>Deux clics pour plus d’intimité :</h3><img src="../../plugins/plxShariff/lang/img/tn_2klick_mf_SC.jpg" />
<p>Les boutons de partage restent désactivés et ne transmettent pas les données personnelles à Facebook, Google et Twitter - jusqu'à ce que l’utilisateur les utilises.</p>
<p>Les boutons de partages bizares qui transmettent les données déjà lors de l’appel, il y a les solutions de rechange amies de la confidentialité qui protègent la vie privée. L’une d’elles est la solution à deux clics, que nous avons lancé il y a trois ans que cela a été présenté dans ct magazine[1]. Sous la devise « Deux clics pour plus d’intimité », la solution s’est propagé rapidement : Comment des milliers d’autres sites Internet utilisent notre technologie, y compris autour des portails de la ville de Munich et Stuttgart.</p>
<p>Dans les version à deux clics, l’utilisateur appelle les boutons réels de partages uniquement en cliquant dessus. Auparavant, aucun transfert de données n’a lieu ; un suivi par des tiers est impossible - du moins pas sur les boutons. En cliquant l'utilisateur accepte l'accord de transfert de données et peut alors recommander des contenus, partager, tweeter, courieler.</p>
<p>Cette solution est toujours une alternative viable aux boutons standards officiels. Cependant, les petits inconvénients pour les opérateurs de sites Web ont révélé en production : sur le coup les boutons ne tentent pas et l'impulsivité à partager du contenu est rompus - le deux clics est juste un seuil. Bien que le modèle de deux clics vous propose, vous pouvez activer en permanence les boutons individuels. Mais grâce à cette commodité se transmet encore une fois, même si les visiteurs ne font pas usage de la fonction.</p>
<p>Le deuxième problème est la conception discrète des boutons de renvoi : la zone grisée illustrent l’état inactif pendant que, saut, mais pas tellement dans l’oeil comme les originaux colorés de Facebook, Twitter et Google. Ce dernier incitera les visiteurs beaucoup plus de partager du contenu. Nos collègues dans le développement Heise Web ont donc pensé et Shariff programmée une version améliorée de la solution de deux clics.</p>
<h3>Un nouveau Shariff en ville</h3>
<p>Les boutons de médias sociaux de Shariff ressortent - et protègent la vie privée de l’utilisateur ainsi que le processus de deux clics. Les nouveaux boutons sont de simples liens HTML, qui sont individuellement avec CSS peut. Un plongement des iframes n’est pas nécessaire pourquoi élimine également l’activation des boutons. Cela permet d’économiser un clic et les parties va un peu plus vite.</p>
<p>Un script obtient, combien de fois une page a été déjà partagée ou tweetée. Il prend en charge à l’application de la programmation des interfaces (API) des services à ce contact et obtient les nombres. La requête est faite par le serveur ; seule l’adresse du serveur sur Facebook, Google et Twitter est transmis au lieu de l’adresse IP du visiteur. Tant que l’utilisateur pas sur le lien permet de partager du contenu, il reste au moins pour Facebook & co. invisible.</p>
<p>Thilo Weichert, commissaire à la protection des données du Schleswig-Holstein, accueille nos nouveaux boutons comme «technologie de confidentialité de l' environnement." Weichert est d'avis que ce genre de boutons de partage devrait être obligatoire pour les opérateurs de site Web pour empêcher les réseaux sociaux "regarder le comportement de l' Internet sans la contribution active de l'utilisateur et sauvegarder" la vie privée.</p>
<h3>Info Shariff Webmaster</h3>
<p>Shariff est le successeur de l'option en deux clics, également développé par Heise et protège votre vie privée. Néanmoins , les parties est très simple avec un seul clic.</p>
<p>Pour les webmasters, il y a Shariff en téléchargement gratuit sur GitHub, voir le lien à la fin de l’article. Pour équiper votre site avec les boutons de la vie privée conforme, télécharger les fichiers nécessaires comme une archive ZIP, rubrique « Presse ». Province de Shariff est composé d’un CSS et un fichier JavaScript. En outre, il existe en plusieurs langues, à savoir les backend PHP et Perl. Une version est disponible également pour Node.js [2] .</p>
<p>Pour la variante la plus simple, vous devez seulement donc utiliser le « front end », shariff.min.js et shariff.min.css. Les boutons contiennent alors aucune information sur la fréquence du contenu est déjà affecté - il ramène seulement en option. Vous décompressez le contenu de l’archive et télécharger les fichiers sur votre serveur. Inclure le fichier CSS dans des documents HTML dans la section &lt;head&gt; et directement le fichier JavaScript avant la balise de fin du corps du document &lt;/body&gt;. Dans tous les lieux où la fonction des boutons devrait être vu, vous placez un &lt;div&gt; conteneur avec la classe shariff. Dans sa forme la plus simple, le code source ressemble à ceci :</p>
<pre>
&lt;!DOCTYPE html&gt;
&lt;html&gt;
&lt;head&gt;
    [...]
    &lt;title&gt;Mon beau site&lt;/title&gt;
    &lt;link href="shariff.min.css" rel="stylesheet"&gt;
&lt;/head&gt;
&lt;body&gt;
    &lt;h1&gt;Mon Produit&lt;/h1&gt;
    &lt;p&gt;[...]&lt;/p&gt;
    &lt;div class="shariff"&gt;&lt;/div&gt;
    &lt;script src="shariff.min.js"&gt;&lt;/script&gt;
&lt;/body&gt;
&lt;/html&gt;
</pre>
<h3>Liens d’information</h3>
<p>liens simples transmettent le contenu à Facebook, Google+ et Twitter. Chaque service prend pour recevoir les information du nombre de like, des paramètres appropriés. Sur Twitter cela ressemble à ceci:</p>
<pre>
https://twitter.com/intent/tweet?text=[...]&url=⤦
 [...]&via=[...]
</pre>
<p>Le paramètre url transmet le texte, l'adresse de page et propose un message qui apparaît comme un Tweet. Parmi via peut spécifier un compte Twitter comme source, soit par exemple @heiseonline pour le contenu de heise.de. De même, qui s’étend aux jetons des liens au niveau de Facebook et Google+. Il envoi u ou l'URL de la page que vous souhaitez partager:</p>
<pre>
https://www.facebook.com/sharer/sharer.php?u=[...]
https://plus.google.com/share?url=[...]
</pre>
<p>
 Ces liens statiques peuvent être affichés facilement dans une page Web
en personnalisant via HTML et CSS. 
Inutile d'entrer l’URL à la main chaque liens,
l’adresse de la page en cours est identifiée à l’aide du JavaScript
de Shariff et en écrit le code source.
Soit le lien de la barre d’adresse du navigateur est utilisé
- mais si un lien est disponible dans la section head du document 
avec rel = canonical définie, Cette « URL canonique » est utilisé.
Le contenu des sites Web utilise des URL différentes pour la même page. 
Ainsi, est appelé à l’aide cette « URL canonique » par les moteurs de recherche
qui du coup savent quelle URL ils doivent de préférence indexer.
Par exemple, Amazon a bien ajusté les paramètres URL de' son code source
pour être Google-friendly (ndt : et pour éviter le duplicate content).</p>
<pre>
uri: function() {
     var uri = global.document.location.href;
     var canonical = $('link[rel=canonical]').attr('href');
     if (canonical && canonical.length &gt; 0) {
          if (canonical.indexOf('http') &lt; 0) {
               canonical = global.document.location.protocol + '//' + global.document.location.host + canonical;
          }
          uri = canonical;
     }
     return uri;
}
</pre>
<p>Les variables et uri canonique sont remplies avec l'URL. La première instruction if vérifie si le parametre url canonique est vide de caractère (null) - donc inexistant dans ce code source du tout. Le second test vérifie si les premiers caractères contiennent un « http », comprenent également le https://. La méthode indexOf () renvoie la position d'une chaîne; dans ce cas la, recherche « http ». Si la valeur est introuvable, Plonge dans le code, la méthode recrache la valeur -1, ce qui est inférieur à zéro. Dans ce cas , l'URL de document.location est assemblé et stocké sous forme uri. Le script lit l'URL et le titre de la page et intègre dans les tweets Twitter.</p>
<p>Enfin Shariff écrit le code des boutons de partage dans la page. Dont le code source est <pre>&lt;div class="shariff"&gt; &lt;/div&gt;</pre> Pour Toutefois en savoir plus, regarder l'élément avec les outils de développement du navigateur, vous pouvez également voir le code JavaScript produit:</p>
<pre>
&lt;ul&gt;
     &lt;li class="button facebook"&gt;
          &lt;a href="https://www.facebook.com/sharer/
 sharer.php?u=http://heise.de/"&gt;&lt;span 
 class="share_text"&gt;Partager&lt;/span&gt;&lt;/a&gt;
     &lt;/li&gt;
     [...]
&lt;/ul&gt;
</pre>
<p>Les boutons sont disposés comme des éléments de liste - contenu dans une balise &lt;ul&gt;. Un nouveau style peut être crée avec un fichier shariff.css. Classes des boutons facebook, Google plus et Twitter donner votre look aux liens. Vous pouvez attribuer différentes couleurs de fond, ajuster la taille des caractères et plus. Une version grise, coloré et blanche des boutons est déjà installé et peut être activé avec l'attribut data: <pre><div class="shariff" data-theme="grey"></div></pre> Avec l'attributs data-services, vous pouvez également spécifier quels boutons doivent être visibles. Leur commande d'orientation avec l'attribut data-orientation; la valeur par défaut est horizontal.</p>
<p>Rendre les boutons plus clair, que les logos des services complètent le texte. Il n’est pas seulement question des graphismes mais les géniales polices d’icônes vectorielles flexible. Vous pouvez mettre sur votrer propre serveur, ou en lien vers un réseau de distribution de contenu MaxCDN ou autre. Les icônes ont l’avantage de pouvoir être mis à l'échelle, restent belles également sur les écrans haute résolution.</p>
<h3>Profiter des délices</h3><img src="../../plugins/plxShariff/lang/img/tn_ct.jpg" />
<p>Pour partager et protéger : Shariff est disponible sous licence MIT sur GitHub en téléchargement libre et gratuit.</p>
<p>Comment beaucoup de personnes ont déjà utilisé le bouton partager sur une page Web, déterminer le composant serveur en option. Télécharger la version apprprié; les backends disponibles sur GitHub sont en : Perl, PHP et Node.js. Si votre site est un grand hoster de Masse, la version PHP est le plus facile à mettre en place. PHP en version 5.4 minimum. Faites glisser le contenu du dossier « build » sur votre serveur, par exemple, dans un nouveau répertoire nommé shariff-backend. Ensuite, entrez l’URL de votre backend comme un attribut de données data-backend-url dans la div conteneur :</p>
<pre>
&lt;div class="shariff" data-backend-url="/shariff-backend/"&gt;&lt;/div&gt;
</pre>
<p>Pour la version PHP, le fichier backend.php est le cœur du composant serveur. Il y a aussi des modules externalisés pour Facebook, Google+ et Twitter. Grâce à leur conception modulaire, les modules de réseaux sociaux peuvent etre facile à ajouter. Le backend envoie une requête vers le serveur des services via les API. Ils offrent une réponse en fonction du fournisseur avec plus ou moins de métadonnées en retour - selon le fournisseur du bouton "partager", mais seul intéresse combien de fois une URL a déjà été partagée. Cette information est extraite du script PHP à partir des données et les stocke pendant 60 secondes dans le cache de fichiers avant que le script récupère de nouveau leur nombre via les API. L’information mise en cache garantit une reponse rapide des résultats, car souvent la réponse aux requêtes des réseaux sociaux est relativement plus lente.</p>
<h3>aides de Shariff</h3>
<p>Comme avec la solution en deux clic, nous vous invitons à essayer notre méthode et l' adapter aux besoins propres et l’améliorer. Pourrait, par exemple, un Plugin PluXml, ce qui le rendrai facile à utiliser dans les blogs.</p>
<p>nous avons publié Shariff sous la licence MIT. Ainsi, nous vous encourageons a utiliser le projet à des fins privées mais aussi commerciales. Vous trouverez tous les fichiers nécessaires pour l’installation et la mise au point sur la page du projet GitHub. Vous y trouverez Shariff à télécharger et des instructions détaillées pour l’installation des backends Perl et Node.js dans votre site. Les développeurs peuvent cloner le contenu ou bifurquer de pleine joie et selon leur cœur ou soumettre des fourches et validation propre.</p>
